#include<iostream>
using namespace std;
int main(){
    // << is called insertion opertor
    int num1,num2;
    cout<<"Enter the value of num1 :\n";
    // >> is called Extraction opertor
    cin>>num1;
    cout<<"Enter the value of num2 :\n";
    cin>>num2;
    cout<<"The sum is "<< num1+num2;
    return 0;
}
